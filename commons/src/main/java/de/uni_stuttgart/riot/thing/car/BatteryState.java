package de.uni_stuttgart.riot.thing.car;

/**
 * Enum for battery state. 
 *
 */
public enum BatteryState {
    
    /**
     * The battery is fully charged.
     */
    FULL,
    
    /**
     * The battery is half charged.
     */
    MIDDLE,
    
    /**
     * The battery is on low charge.
     */
    LOW,
    
    /**
     * The battery is defect.
     */
    DEFECT;
    
}
